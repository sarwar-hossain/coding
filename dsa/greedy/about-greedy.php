        <h1>Greedy Algorithm</h1>
        <p>A <strong>Greedy Algorithm</strong> is a problem-solving approach that builds up a solution piece by piece, always choosing the next piece that offers the most immediate benefit, without worrying about the overall structure.</p>

        <h2>Why Use Greedy Algorithms?</h2>
        <ul>
            <li><strong>Simple and Efficient:</strong> Greedy algorithms tend to be easier to implement and often run faster than other algorithms because they focus on making local optimal choices.</li>
            <li><strong>Optimal for Specific Problems:</strong> For certain types of problems, such as interval scheduling or minimum spanning tree, greedy algorithms provide the optimal solution.</li>
            <li><strong>Iterative Process:</strong> Greedy algorithms typically proceed by repeatedly selecting the locally optimal choice in an iterative manner.</li>
        </ul>

        <h2>Steps in a Greedy Algorithm</h2>

        <div class="operation">
            <h3>1. Selection of Choices</h3>
            <p>At each step, the algorithm selects the best choice from the available options, based on a criterion or greedy property.</p>
        </div>

        <div class="operation">
            <h3>2. Feasibility Check</h3>
            <p>After selecting a choice, the algorithm ensures that it is a feasible solution (i.e., it satisfies the problem's constraints).</p>
        </div>

        <div class="operation">
            <h3>3. Solution Construction</h3>
            <p>The selected choices are combined to build up a solution incrementally, moving towards the goal.</p>
        </div>

        <div class="operation">
            <h3>4. Optimization</h3>
            <p>The greedy algorithm tries to optimize the solution by making locally optimal decisions at each stage.</p>
        </div>

        <h2>Common Applications of Greedy Algorithms</h2>
        <ul>
            <li><strong>Activity Selection Problem:</strong> Choosing the maximum number of activities that don't overlap.</li>
            <li><strong>Huffman Encoding:</strong> A compression algorithm that uses a greedy approach to create an optimal prefix code.</li>
            <li><strong>Fractional Knapsack Problem:</strong> Maximizing profit by selecting items with the highest value-to-weight ratio.</li>
            <li><strong>Dijkstra's Algorithm:</strong> Finding the shortest path from a source node to other nodes in a weighted graph.</li>
            <li><strong>Prim's Algorithm:</strong> Finding the minimum spanning tree of a graph by adding the smallest edge at each step.</li>
        </ul>

        <h2>Greedy Choice Property</h2>
        <p>The <strong>greedy choice property</strong> is crucial for a greedy algorithm to be effective. It states that making the locally optimal choice at each step will lead to the global optimal solution.</p>

        <h2>Example Problem: Fractional Knapsack</h2>
        <p>The Fractional Knapsack Problem involves selecting items to maximize total value without exceeding the weight capacity. A greedy approach selects items based on their value-to-weight ratio.</p>
        <pre><code>
// Example of Greedy Approach in C++
#include &lt;iostream&gt;
#include &lt;vector&gt;
#include &lt;algorithm&gt;

struct Item {
    int value, weight;
};

// Comparison function to sort by value-to-weight ratio
bool cmp(struct Item a, struct Item b) {
    double r1 = (double)a.value / a.weight;
    double r2 = (double)b.value / b.weight;
    return r1 > r2;
}

double fractionalKnapsack(int W, struct Item arr[], int n) {
    std::sort(arr, arr + n, cmp);

    int curWeight = 0;
    double finalValue = 0.0;

    for (int i = 0; i < n; i++) {
        if (curWeight + arr[i].weight <= W) {
            curWeight += arr[i].weight;
            finalValue += arr[i].value;
        } else {
            int remain = W - curWeight;
            finalValue += arr[i].value * ((double)remain / arr[i].weight);
            break;
        }
    }
    return finalValue;
}

int main() {
    int W = 50;
    Item arr[] = {{60, 10}, {100, 20}, {120, 30}};
    int n = sizeof(arr) / sizeof(arr[0]);

    std::cout << "Maximum value: " << fractionalKnapsack(W, arr, n) << std::endl;
    return 0;
}
