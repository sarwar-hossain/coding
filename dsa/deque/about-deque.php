        <h1>Deque in C++ (STL)</h1>
        <p>A <strong>deque</strong> (double-ended queue) is a standard container in C++ that allows fast insertion and deletion of elements at both ends. It combines the advantages of a vector and a list, providing efficient access and modification operations at both the front and the back.</p>

        <h2>Why Use Deque?</h2>
        <ul>
            <li><strong>Fast Insertions and Deletions:</strong> Allows efficient operations at both ends, unlike vectors which are optimized for operations at the end only.</li>
            <li><strong>Random Access:</strong> Supports constant time random access to elements, similar to vectors.</li>
            <li><strong>Flexible:</strong> Useful for scenarios where elements are frequently added or removed from both ends of the sequence.</li>
        </ul>

        <h2>Operations on Deque</h2>

        <div class="operation">
            <h3>1. Initialization</h3>
            <p>Deques can be initialized in various ways:</p>
            <ul>
                <li><strong>Default Initialization:</strong> <code>std::deque&lt;int&gt; d;</code> - Creates an empty deque.</li>
                <li><strong>Initialization with Values:</strong> <code>std::deque&lt;int&gt; d = {1, 2, 3};</code> - Initializes the deque with specific values.</li>
                <li><strong>Initialization with Size:</strong> <code>std::deque&lt;int&gt; d(5, 10);</code> - Initializes a deque with 5 elements, each having the value 10.</li>
            </ul>
        </div>

        <div class="operation">
            <h3>2. Adding Elements</h3>
            <p>Elements can be added to the deque using:</p>
            <ul>
                <li><strong>Push Back:</strong> <code>d.push_back(4);</code> - Adds an element to the back of the deque.</li>
                <li><strong>Push Front:</strong> <code>d.push_front(0);</code> - Adds an element to the front of the deque.</li>
            </ul>
        </div>

        <div class="operation">
            <h3>3. Accessing Elements</h3>
            <p>To access elements in a deque, use:</p>
            <ul>
                <li><strong>At:</strong> <code>int value = d.at(2);</code> - Returns a reference to the element at a specific position with bounds checking.</li>
                <li><strong>Operator[]:</strong> <code>int value = d[2];</code> - Returns a reference to the element at a specific position without bounds checking.</li>
                <li><strong>Front:</strong> <code>int front = d.front();</code> - Returns a reference to the first element.</li>
                <li><strong>Back:</strong> <code>int back = d.back();</code> - Returns a reference to the last element.</li>
            </ul>
        </div>

        <div class="operation">
            <h3>4. Removing Elements</h3>
            <p>To remove elements from the deque, use:</p>
            <ul>
                <li><strong>Pop Back:</strong> <code>d.pop_back();</code> - Removes the last element from the deque.</li>
                <li><strong>Pop Front:</strong> <code>d.pop_front();</code> - Removes the first element from the deque.</li>
            </ul>
        </div>

        <div class="operation">
            <h3>5. Size and Empty Check</h3>
            <p>Check the size and whether the deque is empty with:</p>
            <ul>
                <li><strong>Size:</strong> <code>size_t size = d.size();</code> - Returns the number of elements in the deque.</li>
                <li><strong>Empty:</strong> <code>bool isEmpty = d.empty();</code> - Checks if the deque is empty.</li>
            </ul>
        </div>

        <h2>Use Cases</h2>
        <p>Deques are useful in various scenarios, including:</p>
        <ul>
            <li><strong>Buffer Management:</strong> Managing buffers where data is frequently added or removed from both ends.</li>
            <li><strong>Task Scheduling:</strong> Implementing task schedulers where tasks are added or removed from both ends.</li>
            <li><strong>Dynamic Data Structures:</strong> Implementing dynamic data structures that require frequent updates at both ends.</li>
        </ul>
